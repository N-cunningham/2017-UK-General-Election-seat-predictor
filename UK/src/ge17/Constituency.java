package ge17;

public class Constituency {
	
	float con;
	float lab;
	float lib;
	float ukip;
	float green;
	float SNP;
	float plaid;
	float ind;
	

	
	public Constituency(float con, float lab, float lib, float ukip, float green, float SNP, float plaid, float ind){
		
		this.con = con;
		this.lab = lab;
		this.lib = lib;
		this.ukip = ukip;
		this.green = green;
		this.SNP = SNP;
		this.plaid = plaid;
		this.ind = ind;
		
	}
	
	
	
	
	
}
